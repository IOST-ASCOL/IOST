//? Data Types in JavaScript

// Primitive Types
let name = "Alice"; // String
let age = 25;       // Number
let isMarried = false; // Boolean
let salary = null;  // Null (intentional absence of value)
let marks;          // Undefined (no value assigned)
console.log(typeof marks); // Output: undefined
let id = Symbol("id"); // Symbol

// Non-Primitive Types
let hobbies = ["Reading", "Traveling", "Coding"]; // Array
let person = { firstName: "John", lastName: "Doe" }; // Object

console.log(hobbies);
console.log(person);