//? Conditional Statements

// 1. if statement
let x = 10;
if (x > 5) {
  console.log("x is greater than 5");
}

// 2. if-else statement
let age = 18;
if (age >= 18) {
  console.log("You are an adult");
} else {
  console.log("You are a minor");
}

// 3. switch statement
let fruit = "Banana";
switch (fruit) {
  case "Apple":
    console.log("You chose an Apple");
    break;
  case "Banana":
    console.log("You chose a Banana");
    break;
  default:
    console.log("Invalid choice");
}