package main // Declares the package for the program; "main" is the special package that defines the entry point of the executable.

// Main function
import "fmt" // Imports the "fmt" package, which is part of the Go standard library. It provides functions for formatted I/O, like printing to the console.

func main() { // Defines the main function, the starting point of execution in a Go program.
	fmt.Println("Helloo World") // Uses the Println() function from the "fmt" package to print "Helloo World" followed by a newline.
}
