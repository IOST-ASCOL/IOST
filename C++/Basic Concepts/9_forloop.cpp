#include <iostream>
using namespace std;

// A for loop is used to repeat a block of code a specific number of times.
// Syntax:
// for (initialization; condition; increment/decrement) {
//     // Code to be executed
// }

int main() {
    // Example: Print numbers from 1 to 5 using a for loop
    for (int i = 1; i <= 5; i++) {
        cout << "Iteration: " << i << endl;
    }

    return 0;
}